    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try
        {
            Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
            Connection connObj = DriverManager.getConnection("jdbc:odbc:Phonebook","","");
            Statement stmtObj = connObj.createStatement();

            DefaultListModel model = new DefaultListModel();
            String username = txtUsername.getText();
            String password = txtPassword.getText();

            ResultSet resObj = stmtObj.executeQuery("Select COUNT(*) as data From Admin Where Username = '"+ username +"' and Password = '"+ password +"'  ");
            if (resObj.next()) {
                if(Integer.parseInt(resObj.getString("data")) > 0)
                {
                    PhoneBook obj = new PhoneBook();
                    obj.show();
                    this.hide();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Invalid Username");
                }
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

